// !== - not equal operator
// < - less than operator
// > - greater than operator
// <= less than or equal operator
// >= greater than or equal to operator

let temp = 101

if(temp <= 32){
    console.log('It\'s freezing outside')
} 
if(temp >= 100){
    console.log('It\'s hot outside')
}

